<template>
  <div class="ai-model-selector">
    <!-- Ê†áÈ¢òÊ†è -->
    <div class="selector-header">
      <span class="header-label">AICodeHub</span>
    </div>

    <!-- Âä†ËΩΩÁä∂ÊÄÅ -->
    <div v-if="loading" class="loading-state">
      <a-spin size="small" />
      <span>Âä†ËΩΩ‰∏≠...</span>
    </div>

    <!-- Ê®°ÂûãÂàóË°® - Áõ¥Êé•Âú®ÁªÑ‰ª∂ÂÜÖÊªöÂä®ÔºåÊó†È¢ùÂ§ñÂµåÂ•ó -->
    <div
      v-for="model in models"
      :key="model.modelKey"
      class="model-item"
      :class="{ selected: selectedModelKey === model.modelKey }"
      @click="handleModelSelect(model)"
      v-else
    >
      <!-- Â∑¶‰æßÔºöSVGÂõæÊ†á -->
      <div class="model-icon">
        <img
          :src="getModelIcon(model)"
          :alt="model.provider"
          class="icon-img"
        />
      </div>

      <!-- ‰∏≠Èó¥ÔºöÊ®°ÂûãÂêçÁß∞ -->
      <div class="model-info">
        <span class="model-name">{{ model.modelKey }}</span>
      </div>

      <!-- Âè≥‰æßÔºöËÉΩÂäõÂõæÊ†áÁªÑ + Token -->
      <div class="model-meta">
        <!-- ËÉΩÂäõÂõæÊ†áÁªÑ -->
        <div class="capability-icons">
          <img
            v-for="(iconData, index) in getCapabilityIcons(model)"
            :key="index"
            :src="iconData.icon"
            :alt="iconData.title"
            :title="iconData.title"
            class="capability-icon"
            :class="iconData.type"
          />
        </div>

        <!-- Ë¥®ÈáèÁ≥ªÊï∞ -->
        <div class="token-count">
          {{ formatTokenCount(model) }}
        </div>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { ref, onMounted, computed, watch } from 'vue'
import { message } from 'ant-design-vue'
import { listEnabledModels } from '@/api/aImoxingpeizhi'

// ÂØºÂÖ•SVGÂõæÊ†á
import deepseekIcon from '@/assets/deepseek-color.svg'
import qwenIcon from '@/assets/qwen-color.svg'
import openaiIcon from '@/assets/openai.svg'
import kimiIcon from '@/assets/kimi-color.svg'

// ÂØºÂÖ•ÂäüËÉΩSVGÂõæÊ†á
import viewIcon from '@/assets/view.svg'
import onlineSearchIcon from '@/assets/Online search.svg'
import thinkingIcon from '@/assets/thinking.svg'
import toolsCallIcon from '@/assets/ToolsCall.svg'

// Props
interface Props {
  defaultModelKey?: string
  disabled?: boolean
}

const props = withDefaults(defineProps<Props>(), {
  defaultModelKey: 'codex-mini-latest',
  disabled: false
})

// Emits
const emit = defineEmits<{
  (e: 'change', modelKey: string, model: API.AiModelConfig): void
}>()

// State
const loading = ref(false)
const models = ref<API.AiModelConfig[]>([])
const selectedModelKey = ref<string>(props.defaultModelKey)

// Computed
const currentModel = computed(() => {
  return models.value.find(m => m.modelKey === selectedModelKey.value)
})

// Methods
const getModelIcon = (model: API.AiModelConfig) => {
  const provider = model.provider?.toLowerCase() || ''
  const modelKey = model.modelKey?.toLowerCase() || ''

  // Ê†πÊçÆmodelKeyÂåπÈÖçÂõæÊ†áÔºà‰ºòÂÖàÁ∫ßÊõ¥È´òÔºâ
  if (modelKey.includes('deepseek')) {
    return deepseekIcon
  } else if (modelKey.includes('qwen') || modelKey.includes('coder')) {
    return qwenIcon
  } else if (modelKey.includes('gpt') || modelKey.includes('codex') || modelKey.includes('o3') || modelKey.includes('o4')) {
    return openaiIcon
  } else if (modelKey.includes('kimi')) {
    return kimiIcon
  }

  // Ê†πÊçÆproviderÂåπÈÖçÂõæÊ†á
  if (provider === 'openai' || provider === 'openrouter') {
    return openaiIcon
  } else if (provider === 'iflow') {
    // iflowÊèê‰æõÂ§öÁßçÊ®°ÂûãÔºåÈúÄË¶ÅÊ†πÊçÆmodelKeyÂà§Êñ≠
    if (modelKey.includes('qwen')) return qwenIcon
    if (modelKey.includes('kimi')) return kimiIcon
    if (modelKey.includes('deepseek')) return deepseekIcon
  }

  // ÈªòËÆ§ËøîÂõûOpenAIÂõæÊ†á
  return openaiIcon
}

const loadModels = async () => {
  loading.value = true
  try {
    const response = await listEnabledModels()
    const res = response.data // axiosÂìçÂ∫îÈúÄË¶ÅËÆøÈóÆ.data
    if (res.code === 0 && res.data) {
      models.value = res.data
      // Â¶ÇÊûúÈªòËÆ§Ê®°Âûã‰∏çÂú®ÂàóË°®‰∏≠,ÈÄâÊã©Á¨¨‰∏Ä‰∏™
      const hasDefault = models.value.some(m => m.modelKey === selectedModelKey.value)
      if (!hasDefault && models.value.length > 0) {
        selectedModelKey.value = models.value[0].modelKey || ''
      }
    } else {
      message.error('Âä†ËΩΩÊ®°ÂûãÂàóË°®Â§±Ë¥•: ' + (res.message || 'Êú™Áü•ÈîôËØØ'))
    }
  } catch (error: any) {
    console.error('Âä†ËΩΩÊ®°ÂûãÂàóË°®ÂºÇÂ∏∏:', error)
    const errorMsg = error?.response?.data?.message || error?.message || 'ÁΩëÁªúËØ∑Ê±ÇÂ§±Ë¥•'
    message.error('Âä†ËΩΩÊ®°ÂûãÂàóË°®Â§±Ë¥•: ' + errorMsg)
  } finally {
    loading.value = false
  }
}

const handleModelSelect = (model: API.AiModelConfig) => {
  if (props.disabled) return
  selectedModelKey.value = model.modelKey || ''
  emit('change', model.modelKey || '', model)
  console.log('ÈÄâÊã©Ê®°Âûã:', model.modelName, model.modelKey)
}

const getTierColor = (tier?: string) => {
  switch (tier) {
    case 'SIMPLE':
      return 'green'
    case 'MEDIUM':
      return 'blue'
    case 'HARD':
      return 'orange'
    case 'EXPERT':
      return 'red'
    default:
      return 'default'
  }
}

const getTierLabel = (tier?: string) => {
  switch (tier) {
    case 'SIMPLE':
      return 'ÁÆÄÂçï'
    case 'MEDIUM':
      return '‰∏≠Á≠â'
    case 'HARD':
      return 'Âõ∞Èöæ'
    case 'EXPERT':
      return '‰∏ìÂÆ∂'
    default:
      return tier
  }
}

// Ëé∑ÂèñÂìÅÁâåÁ±ªÂêçÔºàÁî®‰∫éÂìÅÁâåÈÖçËâ≤Ôºâ
const getBrandClass = (model: API.AiModelConfig) => {
  const provider = model.provider?.toLowerCase() || ''
  const modelKey = model.modelKey?.toLowerCase() || ''

  // ‰ºòÂÖàÊ†πÊçÆmodelKeyÂà§Êñ≠
  if (modelKey.includes('deepseek')) {
    return 'deepseek'
  } else if (modelKey.includes('qwen') || modelKey.includes('coder')) {
    return 'qwen'
  } else if (modelKey.includes('gpt') || modelKey.includes('codex') || modelKey.includes('o3') || modelKey.includes('o4')) {
    return 'openai'
  } else if (modelKey.includes('kimi')) {
    return 'kimi'
  } else if (modelKey.includes('gemini')) {
    return 'gemini'
  }

  // Ê†πÊçÆproviderÂà§Êñ≠
  if (provider === 'openai' || provider === 'openrouter') {
    return 'openai'
  } else if (provider === 'google') {
    return 'gemini'
  } else if (provider === 'iflow') {
    return 'default'
  }

  return 'default'
}

// Ëé∑ÂèñÊ®°ÂûãÊòæÁ§∫ÂêçÁß∞
const getModelDisplayName = (model: API.AiModelConfig) => {
  const modelKey = model.modelKey || ''

  // ÊèêÂèñÂìÅÁâåÂêçÁß∞
  if (modelKey.toLowerCase().includes('deepseek')) {
    return 'DeepSeek'
  } else if (modelKey.toLowerCase().includes('qwen')) {
    return 'Qwen'
  } else if (modelKey.toLowerCase().includes('gpt')) {
    return 'GPT'
  } else if (modelKey.toLowerCase().includes('o3') || modelKey.toLowerCase().includes('o4')) {
    return 'OpenAI'
  } else if (modelKey.toLowerCase().includes('kimi')) {
    return 'Kimi'
  }

  return model.modelName || modelKey
}

// Ëé∑ÂèñËÉΩÂäõÂõæÊ†áÁªÑÔºàËøîÂõûÂõæÊ†áÊï∞ÁªÑÔºâ
const getCapabilityIcons = (model: API.AiModelConfig) => {
  const icons: Array<{ icon: string; type: string; title: string }> = []
  const modelKey = model.modelKey?.toLowerCase() || ''

  // GPT-5Á≥ªÂàóÂíåCodexÁ≥ªÂàó - ÂÖ®ËÉΩÂûã(view üåê ‚ùÑÔ∏è üîß)
  const isGpt5 = modelKey.includes('gpt-5') || modelKey.includes('codex')

  // DeepSeekÁ≥ªÂàó - Êé®ÁêÜ+Â∑•ÂÖ∑Âûã(‚ùÑÔ∏è üîß)
  const isDeepSeek = modelKey.includes('deepseek')

  // Qwen3 Coder„ÄÅKimiÁ≥ªÂàó - Â∑•ÂÖ∑‰∏ìÁ≤æÂûã(üîß)
  const isToolOnly = modelKey.includes('qwen3-coder') || modelKey.includes('qwen') ||
                     modelKey.includes('kimi')

  if (isGpt5) {
    // ÂÖ®ËÉΩÂûãÊ®°Âûã - GPT-5ÂíåCodexÁ≥ªÂàó
    icons.push({ icon: viewIcon, type: 'vision', title: 'ËßÜËßâ - ÊîØÊåÅÂõæÂÉèËØÜÂà´ÂíåÂ§ÑÁêÜ' })
    icons.push({ icon: onlineSearchIcon, type: 'web', title: 'ËÅîÁΩë - ÊîØÊåÅÂÆûÊó∂ÁΩëÁªúÊêúÁ¥¢' })
    icons.push({ icon: thinkingIcon, type: 'reasoning', title: 'Êé®ÁêÜ - ÊîØÊåÅÂ§çÊùÇÊé®ÁêÜËÉΩÂäõ' })
    icons.push({ icon: toolsCallIcon, type: 'tool', title: 'Â∑•ÂÖ∑ - ÊîØÊåÅÂáΩÊï∞Ë∞ÉÁî®ÂíåÂ∑•ÂÖ∑‰ΩøÁî®' })
  } else if (isDeepSeek) {
    // Êé®ÁêÜ+Â∑•ÂÖ∑Âûã - DeepSeek V3.1/V3.2
    icons.push({ icon: thinkingIcon, type: 'reasoning', title: 'Êé®ÁêÜ - ÊîØÊåÅÂ§çÊùÇÊé®ÁêÜËÉΩÂäõ' })
    icons.push({ icon: toolsCallIcon, type: 'tool', title: 'Â∑•ÂÖ∑ - ÊîØÊåÅÂáΩÊï∞Ë∞ÉÁî®ÂíåÂ∑•ÂÖ∑‰ΩøÁî®' })
  } else if (isToolOnly) {
    // Â∑•ÂÖ∑‰∏ìÁ≤æÂûã - Qwen3 CoderÂíåKimi K2Á≥ªÂàó
    icons.push({ icon: toolsCallIcon, type: 'tool', title: 'Â∑•ÂÖ∑ - ÊîØÊåÅÂáΩÊï∞Ë∞ÉÁî®ÂíåÂ∑•ÂÖ∑‰ΩøÁî®' })
  } else {
    // Êú™ÂàÜÁ±ªÊ®°Âûã,ÈªòËÆ§ÊòæÁ§∫Â∑•ÂÖ∑ËÉΩÂäõ
    icons.push({ icon: toolsCallIcon, type: 'tool', title: 'Â∑•ÂÖ∑ - ÊîØÊåÅÂáΩÊï∞Ë∞ÉÁî®ÂíåÂ∑•ÂÖ∑‰ΩøÁî®' })
  }

  return icons
}

// Ê†ºÂºèÂåñË¥®ÈáèÁ≥ªÊï∞ÊòæÁ§∫
const formatTokenCount = (model: API.AiModelConfig) => {
  // ÊòæÁ§∫Ë¥®ÈáèÁ≥ªÊï∞ÔºåÂèçÊò†Ê®°ÂûãÁöÑÁßØÂàÜÊ∂àËÄóÂÄçÊï∞
  if (model.qualityScore) {
    return `${model.qualityScore}√ó`
  }
  // ÈôçÁ∫ßÊòæÁ§∫ÔºöÂ¶ÇÊûúÊ≤°ÊúâË¥®ÈáèÁ≥ªÊï∞ÔºåÊòæÁ§∫Âü∫Á°ÄÂÄçÁéá
  return model.pointsPerKToken ? `√ó${model.pointsPerKToken}` : '-'
}

// Watch props changes
watch(() => props.defaultModelKey, (newValue) => {
  if (newValue) {
    selectedModelKey.value = newValue
  }
})

// Lifecycle
onMounted(() => {
  loadModels()
})

// Expose
defineExpose({
  selectedModelKey,
  currentModel,
  loadModels
})
</script>

